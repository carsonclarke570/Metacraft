// Block State Flags
#define BLOCK_ID (20) // amount to shift by to get the block ID

#define B_VISIBLE (1 << 19)
#define B_ACTIVE  (1 << 18) // determines if the block is in the active queue
#define B_TAGGED  (1 << 17) // B_SPECIAL ?
#define B_FLUID   (1 << 16)

#define BLOCK_DATA (0xFFu) // mask to get the Block data


// Block Flags
#define BF_SOLID       (  1) // has a physical model
#define BF_STABLE      (  2) // will not be affected by water or contact
#define BF_TRANSLUCENT (  4) // is semi-transparent
#define BF_LUMINOUS    (  8) // may emit light (see tag for value)
#define BF_ACTIVE      ( 16) // may be active (require regular updating) (i.e. Furnace)

#define BF_FULL (BF_SOLID | BF_STABLE)
#define BF_PARTIAL (BF_SOLID | BF_STABLE | BF_TRANSLUCENT)
#define BF_PHYSICAL (BF_SOLID | BF_TRANSLUCENT)
#define BF_DECORATION (BF_TRANSLUCENT)
#define BF_PLANT (BF_TRANSLUCENT)
#define BF_CROP (BF_ACTIVE | BF_TRANSLUCENT)



/*
[31-20] ID
[19]    visible flag
[18]    active flag
[17]    tagged flag
[16]    fluid flag
[15-0]  data
*/
typedef uint32_t Block;



